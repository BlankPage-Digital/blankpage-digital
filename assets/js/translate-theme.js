// Theme settings: define the primary and background colours for each language.
const themes = {
  en: { primary: "#5928B1", background: "#3D2B42" },
  sv: { primary: "#001A8F", background: "#3D4C7B" },
  ro: { primary: "#9B0000", background: "#7A3B3B" }
};

// Function to update text for all elements with data-i18n attribute.
function updateTranslations(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.innerText = translations[lang][key];
    }
  });
}

// Function to update theme colours via CSS variables.
function updateTheme(lang) {
  const theme = themes[lang];
  document.documentElement.style.setProperty('--primary-color', theme.primary);
  document.documentElement.style.setProperty('--background-lang-color', theme.background);
}

// Set up language switcher event listener.
document.getElementById('language-select').addEventListener('change', function() {
  // Remove language classes (if you use them elsewhere)
  document.body.classList.remove('lang-en', 'lang-sv', 'lang-ro');
  var lang = this.value;
  if (lang === 'en') {
    document.body.classList.add('lang-en');
  } else if (lang === 'sv') {
    document.body.classList.add('lang-sv');
  } else if (lang === 'ro') {
    document.body.classList.add('lang-ro');
  }
  // Update translations and theme colours
  updateTranslations(lang);
  updateTheme(lang);
});
